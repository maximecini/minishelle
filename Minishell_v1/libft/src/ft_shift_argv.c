void	ft_shift_argv(int *arc, char **argv, int nb)
{
	int	i;

	while (nb)
	{
		i = 0;
		while (i < *arc)
		{
			argv[i] = argv[i + 1];
			i++;
		}
		(*arc)--;
		nb--;
	}
}
